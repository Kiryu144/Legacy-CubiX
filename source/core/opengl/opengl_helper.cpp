void ShaderDeleter( GLuint shader )
{
	if( shader != 0 )
	{
		glDeleteShader( shader );
	}
};

void ProgramDeleter( GLuint program )
{
	if( program != 0 )
	{
		glDeleteProgram( program );
	}
};

void BufferDeleter( GLuint buffer )
{
	if( buffer != 0 )
	{
		glDeleteBuffers( 1, &buffer );
	}
}

